	---------VISTA CITASDES-----------------------------------
		CREATE VIEW CITASDES AS
		SELECT CITAS.COD_CITAS, CITAS.HI_NDOCUM, ESSALUD.HI_NOMBRE,MEDICOS.COD_MEDICO,MEDICOS.NOM_MEDICO, MEDICOS.ESPE_MEDICO,PROG_MEDIC.FECHA, CITAS.VISIBLE FROM CITAS 
		INNER JOIN ESSALUD ON CITAS.HI_NDOCUM= ESSALUD.HI_NDOCUM
		INNER JOIN PROG_MEDIC ON CITAS.ID_PROG_MEDIC= PROG_MEDIC.ID_PROG_MEDIC
		INNER JOIN MEDICOS ON PROG_MEDIC.COD_MEDICO= MEDICOS.COD_MEDICO;

