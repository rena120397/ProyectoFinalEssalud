package modelo;
public class padecencia {
    private String idpadecencia;
    private String nompadecencia;
    private int tprom;
    private double p;
    

    public String getIdpadecencia() {
        return idpadecencia;
    }

    public void setIdpadecencia(String idpadecencia) {
        this.idpadecencia = idpadecencia;
    }

    public String getNompadecencia() {
        return nompadecencia;
    }

    public void setNompadecencia(String nompadecencia) {
        this.nompadecencia = nompadecencia;
    }

    public int getTprom() {
        return tprom;
    }

    public void setTprom(int tprom) {
        this.tprom = tprom;
    }

    public double getP() {
        return p;
    }

    public void setP(double p) {
        this.p = p;
    } 
}
